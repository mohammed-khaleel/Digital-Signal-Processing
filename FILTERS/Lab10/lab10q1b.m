wc=1;
s=tf('s');

%For N=2
s0=2^(1/2)*(- 1/10 + 1i/10);
s1=2^(1/2)*(- 1/10 - 1i/10);
H1=(power(wc,2))/((s-s0)*(s-s1));
subplot(2,2,1)
pzmap(H1)

%For N=3
s0=(3^(1/2)*1i)/10 - 1/10;
s1=-1/5;
s2=- (3^(1/2)*1i)/10 - 1/10;
H2=(power(wc,3))/((s-s0)*(s-s1)*(s-s2));
subplot(2,2,2)
pzmap(H2)

%For N=4
s0=((2^(1/2) + 2)^(1/2)*1i)/10 - (2 - 2^(1/2))^(1/2)/10;
s1=- (2^(1/2) + 2)^(1/2)/10 + ((2 - 2^(1/2))^(1/2)*1i)/10;
s2=- (2^(1/2) + 2)^(1/2)/10 - ((2 - 2^(1/2))^(1/2)*1i)/10;
s3=- ((2^(1/2) + 2)^(1/2)*1i)/10 - (2 - 2^(1/2))^(1/2)/10;
H3=(power(wc,4))/((s-s0)*(s-s1)*(s-s2)*(s-s3));
subplot(2,2,3)
pzmap(H3)